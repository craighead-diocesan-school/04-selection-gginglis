function moreThanTwo() {









  let number = prompt('What number do you want to compare to 2?')

  if (number > 2) {
    alert(number + ' is bigger than 2')
  } else if (number == 2) {
    alert('That is 2 you numbskull')
  } else if (number < 2) {
    alert(number + ' is smaller than 2')
  }










}


